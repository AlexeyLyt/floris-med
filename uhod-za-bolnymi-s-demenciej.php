        <p>Деменция - это название, используемое для группы болезней, что связаны с состояний мозга. Эти болезни затрудняют запоминание, рассуждение и общение. Наиболее распространенной формой деменции является болезнь Альцгеймера. Несколько лет назад слабоумие часто называли «старением». Хотя деменция обычно затрагивает людей тогда, когда они становятся старше - это не естественная часть старения. Девять из десяти пожилых людей не страдают деменцией. Теперь известно, что это вовсе не нормально и так не должно быть, ведь это вызвано продолжающимся повреждением клеток в мозге и даже молодые люди тоже могут получить слабоумие: у лиц моложе 65 лет это называется ранней или более ранней деменцией.</p>
        <img src="/images/demencija.jpg" alt="Уход за больными с деменцией">
        <h2>Деменция: признаки</h2>
        <p>Симптомы различаются в зависимости от того, какие части мозга поражены, и от стадии заболевания. Наиболее распространенные симптомы включают в себя:</p>
        <ul class="blog-default-ul">
            <li>При деменции нервные клетки мозга постепенно повреждаются или разрушаются. Со временем части мозга начинают сокращаться. Это часто начинается в той части мозга, которая контролирует память, мышление и личность. Другие части головного мозга могут быть затронуты гораздо позже в течение болезни.</li>
            <li>Потеря памяти, в том числе проблемы с выполнением знакомых задач.</li>
            <li>Языковые проблемы, такие как проблемы с произношением слов или пониманием сказанного.</li>
            <li>Трудности с планированием, организацией, концентрацией. Это включает в себя неспособность распознать собственные симптомы.</li>
            <li>Изменения в поведении и личности.</li>
        </ul>
        <p>Деменция ухудшается со временем. Симптомы различаются для каждого человека, но есть <b>три основных этапа</b>, которые могут длиться от месяцев до лет:</p>
        <p><b>I Ранняя стадия:</b></p>
        <ul class="blog-default-ul">
            <li>Человек может казаться забывчивым и иметь изменения в поведении. Тем не менее, он или она все еще может справиться с большинством задач без посторонней помощи.</li>
        </ul>
        <p><b>II Средняя стадия:</b></p>
        <ul class="blog-default-ul">
            <li>Все больше и больше требуется помощь в выполнении повседневных задач. Человек может испытывать трудности с распознаванием друзей и членов семьи, странствовать или заблудиться в знакомых местах. Больные могут также стать беспокойным или капризным.</li>
        </ul>
        <p><b>III Поздняя стадия:</b></p>
        <ul class="blog-default-ul">
            <li>Болезнь Альцгеймера может вызвать серьезные проблемы с памятью, суждением и другими навыками. Помощь необходима почти во всех аспектах повседневной жизни.</li>
        </ul>
        <h2>Деменция: как проявляется?</h2>
        <ul class="blog-default-ul">
            <li>Для человека, страдающей деменцией, нормально забыть, куда он кладет ключи, даже забыть, для чего эти ключи.</li>
            <li>Может забыть слово, как его произносить и о чем он говорил минуту назад.</li>
            <li>Люди с деменцией могут плохо рассуждать и делать неправильный выбор.</li>
            <li>Также могут наблюдаться сильные изменения в характере и поведении.</li>
            <li>Тот, кто считал каждую копейку, внезапно может начать тратить деньги направо и налево.</li>
        </ul>
        <h2>Деменция: как лечить?</h2>
        <p>В настоящее время нет лекарства от деменции, которые помогли бы полностью ликвидировать данное заболевание. Но при надлежащем уходе многие люди могут жить комфортно с этим заболеванием годами.</p>
        <p>Однако лекарства являются ключевой частью улучшение состояния. Некоторые могут замедлить прогрессирование симптомов, таких как потеря памяти. Другие могут помочь в поднятии настроения, помочь осознанию поведения и решить проблемы со сном. Эти лекарства работают, но не для всех.</p>
        <p>Также, как всем известно, физические упражнения полезны для тела и ума. Они могут даже замедлить прогрессирование заболевания! Простые, повторяющиеся действия являются хорошим выбором для тех, кто столкнулся с деменцией.</p>
        <p>Очень важны регулярные посещения врача, ведь они могут помочь людям отслеживать симптомы и общее состояние здоровья.</p>
        <h2>Как правильно общаться с больным деменцией</h2>
        <ul class="blog-default-ul">
            <li>Общайтесь с пациентом в тихой и спокойной манере. Постарайтесь проявлять как можно больше тепла и понимания, невзирая на проблемы с поведением у больного. Не упускайте возможность в минуты просветления взять близкого человека за руку, обнять его.</li>
            <li>Наказывать больного, демонстрировать недовольство или отказываться от общения с ним – бессмысленно. Ведь зачастую уже через несколько минут пациент полностью забывает об инциденте, поэтому все наказывающие мероприятия лишены смысла.</li>
            <li>Сохраняйте спокойствие и самообладание. Все проявления агрессии, обидные слова и высказывания больного – прежде всего симптом, следствие необратимых изменений в мозге. Как бы ни было трудно, старайтесь не вступать в дискуссию, не давайте себя втягивать в спор. Научиться этому непросто, но жизненно необходимо тем, кто осуществляет уход за больными с деменцией.</li>
        </ul>
        <h2>Организация процесса приема пищи</h2>
        <p>По мере ухудшения состояния могут возникать трудности с глотанием, существует риск полного отказа от пищи. Человек может просто забыть, что пищу нужно пережевывать, не помнить ключевых этапов приема пищи, не может выполнять простейшие действия – подносить ложку ко рту и т.п. Все это приводит к ослаблению аппетита и потере веса. Существует и другая крайность – появляется настойчивое желание постоянно что-то жевать, человек постоянно хочет есть и забывает о том, что он недавно уже поел.</p>
        <p>Организация питания больного с деменцией должна происходить с учетом особенностей его состояния. Не следует пытаться накормить его силой, принуждать и заставлять. Садиться за стол следует в спокойной обстановке без отвлекающих факторов – радио или телевизора. Пища не должна быть горячей. Отлично подойдут блюда, которые можно есть руками.</p>
        <hr class="blog-hr">
        <p>Мало кто понимает, что такое деменция. Недостаток знаний и понимания может осложнить жизнь людям, страдающим деменцией, их семьям и опекунам. Очень важно, чтобы люди научились лучше понимать деменцию, чтобы могли оказывать поддержку и помогать людям с этим заболеванием. Это позволяет людям с деменцией участвовать в жизни общества и чувствовать себя лучше.</p>
        <p>Жизнь меняется, когда у кого-то развивается деменция, но она не заканчивается.</p>
