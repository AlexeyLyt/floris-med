        <p class="blog-default-p">У каждого лежачего больного рано или поздно может возникнуть дистрофия кожи, другими словами, появиться пролежни. Отсутствие данной патологии свидетельствует о том, что за пациентом ведется хороший уход.</p>
        <h2 class="blog-default-h2">Пролежни: что способствует их появлению?</h2>
        <p class="blog-default-p">Лежачий пациент практически полностью обездвижен. В одной и той же позе он находится большую часть времени. Именно неподвижность становится причиной патологических изменений в эпидермисе — отклонений, наносящих большой вред здоровью.</p>
        <img src="/images/prolezhni.png" alt="" class="blog-img-default">
        <p class="blog-default-p">Когда человек постоянно находится в одной и той же позе, у него сдавливаются мягкие ткани и кровеносные сосуды. Если лежачий больной пребывает больше двух часов без движения, происходит ишемия — нарушение питания кожи. После чего наступает некроз — отмирание эпидермиса, что ведет к возникновению пролежня.</p>
        <p class="blog-default-p">Возникнуть пролежни могут не только из-за отсутствия подвижности, но и по причине неграмотного ухода. Например, иногда больного тянут по постели, чтобы посадить на судно или провести смену постельных принадлежностей. Из-за этого происходит смещение поверхностного эпидермиса относительно внутреннего. В результате таких действий рвутся мелкие капилляры. Это становится причиной нарушения кровоснабжения данных отделов кожи, появления некроза.</p>
        <p class="blog-default-p">Проблемы с кожей могут возникнуть из-за:</p>
        <ul class="blog-default-ul">
            <li>чрезмерного потоотделения;</li>
            <li>ожирения или, наоборот, истощения;</li>
            <li>анемии;</li>
            <li>диспротеинемии;</li>
            <li>недержания кала, мочи;</li>
        </ul>
        <p class="blog-default-p">Часто пролежни появляются еще во время нахождения больного в лечебном учреждении. Поэтому родственникам и близким людям больного следует обратить особое внимание на этот момент.</p>
        <h2 class="blog-default-h2">Чем опасны пролежни?</h2>
        <p class="blog-default-p">Образование пролежней существенно ухудшает качество жизни пациентов. Ведь помимо местных изменений у них появляются выраженные боли, ухудшается общее самочувствие. Даже повседневный уход и попытки изменить положение тела причиняют страдания.</p>
        <p class="blog-default-p">Основная опасность пролежней – развитие гнойно-септических осложнений. К ним относят рожистое воспаление, флегмону, гангрену, остеомиелит, сепсис и инфекционно-токсический шок. Эти состояния являются потенциально опасными для жизни и требуют максимально интенсивного лечения. Пациент обычно нуждается в госпитализации.</p>
        <h2 class="blog-default-h2">Меры по устранению и быстрому заживлению пролежней</h2>
        <p class="blog-default-p">Профилактика пролежней может включать ряд вмешательств, таких как питание, уход за кожей, использование матрацев с перераспределением давления и репозиционирование. Исключить факторы риска для здоровья больного можно следуя следующим советам:</p>
        <ul class="blog-default-ul">
            <li>Регулярно менять положение тела больного – каждые 2–3 часа;</li>
            <li>Ограничить сдавливание кожи; если появились пролежневые изменения, больной не должен сидеть или лежать, сдавливая воспаленную часть тела;</li>
            <li>Укладывая больного на бок, не забывать помещать подушку под слегка согнутую верхнюю ногу пациента от паховой области до стопы;</li>
            <li>Отказаться от резиновых и клеенчатых подкладок – они скатываются и пережимают кожу, не пропуская к ней воздух;</li>
            <li>Постельное белье не должно быть влажным или мокрым – если больной обмочится в постель, следует снять ее и постелить свежую;</li>
            <li>Следует оказывать помощь больному во избежания травм, в том числе и из-за царапин, – коротко постригите ногти больному и подпилите их, чтобы он не мог причинить себе вреда.</li>
        </ul>
        <p class="blog-default-p"><b>Активизируйте больного, насколько это возможно</b>, побуждая подопечного к выполнению разнообразных действий и постепенно увеличивая разнообразие движений больного.</p>
        <p><b>Обеспечьте тщательный уход за кожей больного</b> используя подсушивающие, антисептические и обезболивающие препараты. Это может быть раствор цинка, перманганат калия. Заживляющие повязки закрепляют бинтом. Использовать пластырь нельзя. Из-за него кожа не может нормально функционировать. При этом при его удалении может произойти повреждение воспаленного раной эпидермиса.</p>
        <p><b>Позаботьтесь о надлежащей диете подопечного</b>: в рацион должны входить злаки, молочные продукты, мясо, рыба, сыры, сухие стручковые семена, овощи и жиры. Следует избегать употребления сладостей, животных жиров и ограничить употребление соли.</p>
