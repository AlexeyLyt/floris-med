        <p>Профессия сиделки всегда была востребованной. Квалифицированная сиделка должна тонко чувствовать все потребности больного, обладать огромным терпением при выполнении его желаний. Современные агентства, которые подбирают домашний персонал, всегда имеют в своём арсенале квалифицированную сиделку. В случае если больному необходима не обыкновенная сиделка, а ещё и интересный собеседник-компаньон, то сиделке необходимо иметь определённый багаж знаний, чтобы престарелый или больной человек мог полноценно общаться. Кроме того, сиделки-компаньоны должны регулярно прогуливаться с больным, разделять его увлечения и интересы.</p>
        <p>Медсестра-сиделка должна обладать медицинскими знаниями и уметь ставить капельницы и уколы, а также проводить все процедуры, прописанные больному. Такие сиделки отвечают за больного вдвойне, потому что должны своевременно оказать медицинскую помощь в случае ухудшения его состояния.</p>
        <p>Также на рынке труда пользуются спросом сиделки-помощницы, которые помогают приготовить больному еду, постирать, убраться. Одни сиделки получают зарплату за каждый день, другие раз в месяц. Также некоторые из них живут в доме своего работодателя, а многие вынуждены приезжать на работу каждый день. В каждом конкретном случае имеется определённый тариф на оказание услуг. Большая разница в цене может заключаться и в квалификации сиделки, и в её профессиональной подготовке.</p>
        <h2>Обязанности сиделки</h2>
        <p>Сиделка – это специалист с высшим или неполным медицинским образованием. Обычно сиделку нанимают для присмотра за пожилыми или больными. Именно таким людям необходим постоянный заботливый уход и внимание, которые не всегда могут предоставить занятые на работе родственники. У такого персонала должно быть медицинское образование, поскольку часто в её обязанности входит следит за состоянием здоровья клиента и в случае необходимости принимать экстренные меры для улучшения его состояния.</p>
        <ul class="blog-default-ul">
