            <li>Отдельные виды производимых работ отдельно оговариваются в контракте. Это может быть и проведение лечебной физкультуры, и медицинские манипуляции (массаж, инъекции).</li>
            <li>Работница может приходить на какое-то определенное время на дежурство или проживать постоянно с подопечным. Если она проживает в жилище больного, то ей предоставляется не менее двух выходных в месяц.</li>
        </ul>
