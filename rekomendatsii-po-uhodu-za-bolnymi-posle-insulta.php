            <div class="second-block-invalid">В нередких случаях людей перенесших инсульт настигает полная или частичная потеря подвижности. Следующие советы помогут в восстановлении и борьбе с недугом:</div>
            <ul class="blog-default-ul blog-default-ul-inside">
                <li>Следует продуманно отнестись к обуви, которую носит пациент - она должна быть комфортной и приспоболенной к частым переобуваниям;</li>
                <li>Место пребывания пациента должно быть обустроенно в лучших мерах безопасности передвижения - отсутсвие отсрых углов, высоких порогов и скользких дорожек, наличие перил;</li>
                <li>Обеспечить больного поддержкой в случаях волеизъявлений к прогулке, не отказывать в сопровождении в случае необходимости во время передвижения по дому.</li>
            </ul>
        </div>
        <div class="flex-container-invalid">
            <div class="first-block-invalid"><span class="count-invalid-4">Общение</span></div>
            <div class="second-block-invalid">Регулярное и правильное общение является одним из главных советов обращения с больным. Поскольку в таких ситуациях больной становится очень чувствительным, его нужно постоянно морально поддерживать и относится с уважением к любым его замечаниям и тревогам.</div>
        </div>
